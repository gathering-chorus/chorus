// 4. BRIEF APPEARS IN SESSION CONTEXT — session-start file includes briefs
// ═══════════════════════════════════════════════════════════════════════════

describe('Flow: Brief delivery to session context', () => {
  test('session-start file references recent briefs', () => {
    // Check any role's session-start file
    for (const role of ['silas', 'kade', 'wren']) {
      const sessionFile = `/tmp/session-start-${role}.md`;
      if (!fs.existsSync(sessionFile)) continue;

      const content = fs.readFileSync(sessionFile, 'utf-8');
      expect(content).toMatch(/Brief|brief/);
      return;
    }
    // At least one session file should exist
    expect(fs.existsSync('/tmp/session-start-silas.md')).toBe(true);
  });

  test('session-start includes brief filenames', () => {
    const sessionFile = '/tmp/session-start-silas.md';
    if (!fs.existsSync(sessionFile)) {
      expect(true).toBe(true);
      return;
    }
    const content = fs.readFileSync(sessionFile, 'utf-8');
    // Should list recent briefs with date-prefixed filenames
    expect(content).toMatch(/\d{4}-\d{2}-\d{2}.*\.md/);
  });
});

// ═══════════════════════════════════════════════════════════════════════════
// 5. STALE BRIEF DETECTION — completed workflow briefs flagged
// ═══════════════════════════════════════════════════════════════════════════

describe('Flow: Stale brief detection', () => {
  test('session-start flags stale workflow briefs', () => {
    const sessionFile = '/tmp/session-start-silas.md';
    if (!fs.existsSync(sessionFile)) {
      expect(true).toBe(true);
      return;
    }
    const content = fs.readFileSync(sessionFile, 'utf-8');
    // Stale briefs section may or may not exist depending on state
    // Just verify the concept is represented
    if (content.includes('STALE-BRIEF') || content.includes('stale brief')) {
      expect(content).toMatch(/STALE|stale/);
    } else {
      // No stale briefs — that's fine too
      expect(true).toBe(true);
    }
  });

  test('SDK notifyOwnerIfDifferent writes brief on card moves', () => {
    // Verify the SDK function creates briefs
    const sdkContent = fs.readFileSync(
      path.join(__dirname, '../src/sdk.ts'),
      'utf-8'
    );
    expect(sdkContent).toContain('notifyOwnerIfDifferent');
    expect(sdkContent).toContain('briefFile');
    expect(sdkContent).toContain('writeFileSync');
  });

  test('SDK notifyPM writes brief on card completion', () => {
    const sdkContent = fs.readFileSync(
      path.join(__dirname, '../src/sdk.ts'),
      'utf-8'
    );
    expect(sdkContent).toContain('notifyPM');
    expect(sdkContent).toContain('shipped');
  });
});
